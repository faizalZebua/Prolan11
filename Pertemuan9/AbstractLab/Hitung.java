public class Hitung extends Calculator {
    protected double hitungTambah(double x, double y){
        return x + y;
    }
    protected double hitungKurang(double x, double y){
        return x - y;
    }
    protected double hitungKali(double x, double y){
        return x * y;
    }
    protected double hitungBagi(double x, double y){
        return x / y;
    }
}
